package com.khaled;

public class Food {
	private String food_type;
	private String food_brand;
	private String food_calories;
	private String food_protein;
	private String food_carbohydrate;
	private String food_fat;
	private String mealType;
	private String food_servings;
	public String getFood_type() {
		return food_type;
	}
	public void setFood_type(String food_type) {
		this.food_type = food_type;
	}
	public String getFood_brand() {
		return food_brand;
	}
	public void setFood_brand(String food_brand) {
		this.food_brand = food_brand;
	}
	public String getFood_calories() {
		return food_calories;
	}
	public void setFood_calories(String food_calories) {
		this.food_calories = food_calories;
	}
	public String getFood_protein() {
		return food_protein;
	}
	public void setFood_protein(String food_protein) {
		this.food_protein = food_protein;
	}
	public String getFood_carbohydrate() {
		return food_carbohydrate;
	}
	public void setFood_carbohydrate(String food_carbohydrate) {
		this.food_carbohydrate = food_carbohydrate;
	}
	public String getFood_fat() {
		return food_fat;
	}
	public void setFood_fat(String food_fat) {
		this.food_fat = food_fat;
	}
	public String getMealType() {
		return mealType;
	}
	public void setMealType(String mealType) {
		this.mealType = mealType;
	}
	public String getFood_servings() {
		return food_servings;
	}
	public void setFood_servings(String food_servings) {
		this.food_servings = food_servings;
	}
    
}
